            const result = await convertToWebP(
                buffer,
                'test.svg',
                'image/svg+xml'
            );

            expect(result.mimeType).toBe('image/svg+xml');
            expect(result.filename).toBe('test.svg');
            expect(result.buffer).toEqual(buffer);
