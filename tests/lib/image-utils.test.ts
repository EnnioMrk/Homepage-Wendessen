    describe('convertDataUrlToWebP', () => {
        test('throws error for invalid data URL format', async () => {
            await expect(
                convertDataUrlToWebP('not-a-data-url')
            ).rejects.toThrow('Invalid data URL format');
        });

        test('throws error for data URL without base64', async () => {
            await expect(
                convertDataUrlToWebP('data:image/png,hello')
            ).rejects.toThrow('Invalid data URL format');
        });

        test('parses valid data URL and converts', async () => {
            // Minimal 1x1 PNG as base64
            const pngBase64 =
                'iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAYAAAAfFcSJAAAADUlEQVR42mNk+M9QDwADhgGAWjR9awAAAABJRU5ErkJggg==';
            const dataUrl = `data:image/png;base64,${pngBase64}`;

            const result = await convertDataUrlToWebP(dataUrl);

            expect(result.mimeType).toBe('image/webp');
            expect(result.filename).toMatch(/\.webp$/);
        });

        test('uses provided filename', async () => {
            const pngBase64 =
                'iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAYAAAAfFcSJAAAADUlEQVR42mNk+M9QDwADhgGAWjR9awAAAABJRU5ErkJggg==';
            const dataUrl = `data:image/png;base64,${pngBase64}`;

            const result = await convertDataUrlToWebP(dataUrl, 'myimage.png');

            expect(result.filename).toBe('myimage.webp');
        });
    });

    describe('bufferToDataUrl', () => {
        test('creates valid data URL from buffer', () => {
            const buffer = Buffer.from('hello world');
            const result = bufferToDataUrl(buffer, 'text/plain');

            expect(result).toMatch(/^data:text\/plain;base64,/);
        });

        test('encodes buffer content correctly', () => {
            const buffer = Buffer.from('test');
            const result = bufferToDataUrl(buffer, 'text/plain');

            // 'test' in base64 is 'dGVzdA=='
            expect(result).toBe('data:text/plain;base64,dGVzdA==');
        });

        test('handles binary data', () => {
            const buffer = Buffer.from([0x00, 0x01, 0x02, 0xff]);
            const result = bufferToDataUrl(buffer, 'application/octet-stream');

            expect(result).toMatch(/^data:application\/octet-stream;base64,/);
        });

        test('handles image mime types', () => {
            const buffer = Buffer.from([0xff, 0xd8, 0xff]); // JPEG magic bytes
            const result = bufferToDataUrl(buffer, 'image/jpeg');

            expect(result).toMatch(/^data:image\/jpeg;base64,/);
        });

        test('handles empty buffer', () => {
            const buffer = Buffer.from('');
            const result = bufferToDataUrl(buffer, 'text/plain');

            expect(result).toBe('data:text/plain;base64,');
        });
    });
